             * Если является - при помощи оператора switch, равно ли число 5, 6, 9 и вывести на консоль например "Строка равна 5"
             * Если нет, при помощи тернарного оператора проверить, больше ли строка 10, если да - присвоить в результат 10 иначе 20
             * И вывести на экран
             * Если строна не является числом - вывести на консоль надпись "Строка не является числом"
             * Для проверки на число использоват int.TryParse()
            */
        }
